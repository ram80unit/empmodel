
function ne = VenusIonosphereDay(alt,ind)

% Daytime Ionosphere for Venus, taken from two profiles in Venus II, 
% page 168; these are from Kliore et al, JGR, 1991.
%
% A(:,:,1) is most dense (high solar activity) profile 
% A(:,:,2) is least dense (low solar activity) profile
%

%       1            2      3
A(:,:,1) = [60.00 0.00000E+01 -1.000;
    100.00 1.00000E+10 -1.000;
    110.00 4.00000E+10 -1.000;
    120.00 1.00000E+11 -1.000;
    130.00 3.00000E+11 -1.000;
    140.00 4.50000E+11 -1.000;
    150.00 3.50000E+11 -1.000;
    160.00 2.50000E+11 -1.000;
    170.00 2.00000E+11 -1.000;
    180.00 1.50000E+11 -1.000;
    190.00 1.20000E+11 -1.000;
    200.00 1.00000E+11 -1.000;
    220.00 7.00000E+10 -1.000;
    240.00 6.00000E+10 -1.000;
    260.00 5.30000E+10 -1.000;
    280.00 4.60000E+10 -1.000;
    300.00 4.00000E+10 -1.000;
    350.00 3.00000E+10 -1.000;
    400.00 2.00000E+10 -1.000;
    450.00 1.50000E+10 -1.000;
    500.00 1.00000E+10 -1.000];

A(:,:,2) = [60.00 0.00000E+01 -1.000;
    100.00 1.00000E+10 -1.000;
    110.00 2.50000E+10 -1.000;
    120.00 6.00000E+10 -1.000;
    130.00 1.00000E+11 -1.000;
    140.00 3.00000E+11 -1.000;
    150.00 1.40000E+11 -1.000;
    160.00 1.00000E+11 -1.000;
    170.00 7.00000E+10 -1.000;
    180.00 5.00000E+10 -1.000;
    190.00 4.00000E+10 -1.000;
    200.00 3.00000E+10 -1.000;
    220.00 1.60000E+10 -1.000;
    240.00 1.00000E+10 -1.000;
    260.00 8.00000E+09 -1.000;
    280.00 7.00000E+09 -1.000;
    300.00 6.00000E+09 -1.000;
    350.00 2.00000E+09 -1.000;
    400.00 1.00000E+09 -1.000;
    450.00 1.00000E+09 -1.000;
    500.00 1.00000E+09 -1.000];


netemp = A(:,2,ind);
netemp(netemp < 0) = 0;
h = A(:,1,ind);

ne = interp1(h,netemp,alt,'cubic');

